CREATE DATABASE BDNoticias;
GO

USE BDNoticias;
GO
CREATE TABLE TBCategoria(
	ID INT IDENTITY,
	DESCRICAO VARCHAR(30)

	CONSTRAINT PK_CATEGORIA PRIMARY KEY (ID)
)

GO
CREATE TABLE TBNoticia(
	ID			 INT IDENTITY,
	TITULO		 VARCHAR(50),
	CONTEUDO	 VARCHAR(50),
	IMAGEM		 VARCHAR(50),
	ID_CATEGORIA INT NOT NULL
	CONSTRAINT PK_NOTICIA PRIMARY KEY(ID),	
	CONSTRAINT FK_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES TBCategoria(ID)
);
GO
CREATE TABLE TBUsuario(
	LOGIN_USUARIO	 VARCHAR(30) NOT NULL,
	SENHA			 VARCHAR(32) NOT NULL,
	NIVEL_ACESSO	 INT NOT NULL DEFAULT 0,
	CONSTRAINT PK_LOGIN_USUARIO PRIMARY KEY(LOGIN_USUARIO), 
	--0-> USUARIO / 1-> ADMINISTRADOR
	CONSTRAINT CK_NIVEL_ACESSO	CHECK (
		(NIVEL_ACESSO = 0) OR (NIVEL_ACESSO = 1) )
);
GO


CREATE TABLE TBClientes(
	ID INT IDENTITY,
	NOME VARCHAR (30),
	SOBRENOME VARCHAR (10),
	EMAIL VARCHAR (30),
	CPF VARCHAR(11),
	RG VARCHAR (9),
	CIDADE VARCHAR (40),
	BAIRRO VARCHAR (30),
	ENDERECO VARCHAR (40),
	UF CHAR (2),
	PROFISSAO VARCHAR (30),
	

	CONSTRAINT PK_CLIENTES PRIMARY KEY (ID),
)
GO


CREATE TABLE TBVotos(

ID INT IDENTITY,
VOTO1 INT NOT NULL,
VOTO2 INT NOT NULL,
VOTO3 INT NOT NULL,
TOTALVOTOS INT NULL
